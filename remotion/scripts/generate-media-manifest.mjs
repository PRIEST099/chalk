import { readdir, writeFile } from "node:fs/promises";
import { join } from "node:path";
import { parseMedia } from "@remotion/media-parser";
import { nodeReader } from "@remotion/media-parser/node";

const root = process.cwd();
const filesIn = async (folder) => {
  try { return (await readdir(join(root, "public", folder))).filter((file) => !file.startsWith(".")); } catch { return []; }
};

// Durations feed src/validate.ts, which refuses renders whose trims outrun their source media.
const probeDurationSec = async (relativePath) => {
  try {
    const { durationInSeconds } = await parseMedia({ src: join(root, "public", relativePath), reader: nodeReader, fields: { durationInSeconds: true }, acknowledgeRemotionLicense: true });
    if (typeof durationInSeconds !== "number") throw new Error("no duration in container metadata");
    return Math.round(durationInSeconds * 100) / 100;
  } catch (error) {
    console.warn(`Could not read the duration of public/${relativePath}: ${error instanceof Error ? error.message : error}`);
    return null;
  }
};

const recordings = await filesIn("recordings");
const audio = await filesIn("audio");
const durations = {};
for (const relativePath of [...recordings.map((file) => `recordings/${file}`), ...audio.map((file) => `audio/${file}`)]) {
  const duration = await probeDurationSec(relativePath);
  if (duration !== null) durations[relativePath] = duration;
}

const source = `// Generated by scripts/generate-media-manifest.mjs. Do not edit by hand.
export const availableRecordingFiles: readonly string[] = ${JSON.stringify(recordings)};
export const availableAudioFiles: readonly string[] = ${JSON.stringify(audio)};
export const mediaDurationsSec: Readonly<Record<string, number>> = ${JSON.stringify(durations)};
`;
await writeFile(join(root, "src", "generated-media.ts"), source, "utf8");
